    <script>
        (function($){
            // product-item 관련
            $(window).ready(function(){
                $('.product-item .sub-slider').slick({
                    autoplay: true,
                    infinite: true,
                    speed: 1200,
                    autoplaySpeed: 3000,
                    dots: true,
                    arrows: false
                });
            });
            
            // product-line 관련
            $(window).on('scroll', function() {
                $('.product-line').each(function(index, elem) {
                    if ($(window).scrollTop() > $(elem).offset().top - ($(window).height() / 1.3)) {
                        $(elem).addClass('action');
                    }
                });
            });
            
            // product-view 관련
            $(document).on('click', '.purifier .list-view', function(){
                $('.purifier').addClass('active');
                $('.purifier-list').addClass('on');
            });
            $(document).on('click', '.humidifier .list-view', function(){
                $('.humidifier').addClass('active');
                $('.humidifier-list').addClass('on');
            });
            $(document).on('click', '.circulator .list-view', function(){
                $('.circulator').addClass('active');
                $('.circulator-list').addClass('on');
            });
            $(document).on('click', '.purifier .list-close', function(){
                $('.purifier').removeClass('active');
                $('.purifier-list').removeClass('on');
            });
            $(document).on('click', '.humidifier .list-close', function(){
                $('.humidifier').removeClass('active');
                $('.humidifier-list').removeClass('on');
            });
            $(document).on('click', '.circulator .list-close', function(){
                $('.circulator').removeClass('active');
                $('.circulator-list').removeClass('on');
            });
        })(jQuery);
    </script>
